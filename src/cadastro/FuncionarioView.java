     public String AbrirImagem() throws IOException {
        
        
        escolherImagem = new JFileChooser();
        
        FileNameExtensionFilter filtro = 
                new FileNameExtensionFilter("Imagem", "jpg", "jpeg", "gif");
        escolherImagem.setFileFilter(filtro);

                
        int resultado = escolherImagem.showOpenDialog(FuncionarioView.this);
                
        if (resultado == JFileChooser.APPROVE_OPTION) {
            
            // armazena o diret√≥rio da imagem.
             FuncionarioView.caminho = escolherImagem.getSelectedFile().getPath();
           labelImagem.setIcon(new ImageIcon(FuncionarioView.caminho));
           
           //redimensiona o label
             ImageIcon icone = (ImageIcon) labelImagem.getIcon();
             Image imagem = icone.getImage();
             Image newimg = imagem.getScaledInstance(118, 111,  java.awt.Image.SCALE_SMOOTH); 
             icone = new ImageIcon(newimg);
             
             //seta o novo label com a imagem redimensionada
             labelImagem.setIcon(icone);
                                               
        }
         return ClienteEditView.caminho;
    }  
    
